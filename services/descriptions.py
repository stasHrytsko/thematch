    @staticmethod
    def get_emoji(value: float) -> str:
        """
        Возвращает эмодзи для заданного значения совместимости
        
        Args:
            value (float): Значение совместимости (0-100)
            
        Returns:
            str: Эмодзи, соответствующий значению
        """
        try:
            value = float(value)
            if not 0 <= value <= 100:
                raise ValueError("Value must be between 0 and 100")
                
            for range_key, emoji in CompatibilityDescriptions.COMPATIBILITY_EMOJI.items():
                if int(value) in range_key:
                    return emoji
            return '😐'
        except (ValueError, TypeError):
            return '😐'

    @staticmethod
    def get_random_phrase(value: float, phrase_dict: Dict[range, List[str]]) -> str:
        """
        Возвращает случайную фразу для заданного значения
        
        Args:
            value (float): Значение совместимости (0-100)
            phrase_dict (Dict[range, List[str]]): Словарь фраз
            
        Returns:
            str: Случайная фраза из соответствующего диапазона
        """
        try:
            value = float(value)
            if not 0 <= value <= 100:
                raise ValueError("Value must be between 0 and 100")
                
            for range_key, phrases in phrase_dict.items():
                if int(value) in range_key:
                    return random.choice(phrases)
            return "Требуется больше данных для анализа."
        except (ValueError, TypeError):
            return "Требуется больше данных для анализа."
        
    @staticmethod
    def get_elements_compatibility_type(element1: str, element2: str) -> str:
        """
        Определяет тип совместимости стихий
        
        Args:
            element1 (str): Первая стихия
            element2 (str): Вторая стихия
            
        Returns:
            str: Тип совместимости ('same_element', 'compatible', или 'neutral')
        """
        if element1 == element2:
            return 'same_element'
        elif (element1 == 'Огонь' and element2 == 'Воздух') or \
             (element1 == 'Воздух' and element2 == 'Огонь') or \
             (element1 == 'Земля' and element2 == 'Вода') or \
             (element1 == 'Вода' and element2 == 'Земля'):
            return 'compatible'
        return 'neutral'

